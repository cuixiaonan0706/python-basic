#转义字符
# \n 换行；\\ 反斜杠幅号；\' 单引号；\"双引号；\t 制表符（相当于tab键，四个空格）

#\n 输出时换行
info= '你的名字是Lucy\n我的名字是Wilia\n我们是朋友'
print(info)

#\t 排版
str1='网站\t\t\t域名\t\t\t\t\t\t\t年龄\t\t价值'
str2='毕站大学\t\thttp://www.blibli.com/\t\t20\t\t50000w'
str3='百度\t\t\twww.baidu.com\t\t\t\t20\t\t2000w'
print(str1)
print(str2)
print(str3)
    #打几个\t需要按实际调整

#字符串内用引号，需要转义
print('we\'re friends')

#练习
print('we\\re')
print('he\tllo')
print('he\'llo')
print('he\"llo')
