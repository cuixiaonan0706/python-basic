# 查找
# 1.通过下标查找数据
t1=('six','nine','ten','one')
print(t1[1])

# 2.index() 查找某个数据，如果数据存在，则返回对应的下标（索引），否则报错
print(t1.index('one'))

# 3.count() 统计某个数据在当前元组中出现的次数,若不存在则返回0
print(t1.count('six'))

# 4.len() 统计元组中数据的个数
print(len(t1))


# 5.拓展：元组“可变的”情况
t2 = (10,20,['six','nine','ten','one'],40,50)
print(t2[2])    #  把中括号堪称一个整体，然后通过下标取值

'''如何提取中括号内的值？'''
print(t2[2][0])
'''中括号内的值可以修改,元组的中括号内的值发生改变'''
t2[2][0]='6'
print(t2)